makeCacheMatrix <- function(x = matrix()) {
  inve <- NULL
  set <- function(y) {
    x <<- y
    inve <<- NULL
  }
  get <- function() x
  setinv <- function(solve) inve <<- solve
  getinv <- function() inve
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}



